def sort_012(input_list):
    counter = [0,0,0]
    for i in input_list:
        counter[i] += 1
    ret = [0] * counter[0] + [1] * counter[1] + [2] * counter[2]
    for i in range(len(input_list)):
        input_list[i] = ret[i]
    return input_list

def test_function(test_case):
    sorted_array = sort_012(test_case)
    print(sorted_array)
    if sorted_array == sorted(test_case):
        print("Pass")
    else:
        print("Fail")

